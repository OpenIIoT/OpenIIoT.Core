using System;

namespace OpenIIoT.SDK.Package
{
    /// <summary>
    ///     Enumeration of the different Package installation options.
    /// </summary>
    [Flags]
    public enum PackageInstallOptions
    {
        /// <summary>
        ///     The default value.
        /// </summary>
        None = 0,

        /// <summary>
        ///     Skip Package verification prior to installation.
        /// </summary>
        SkipVerification = 1,

        /// <summary>
        ///     Overwrite the destination Package, if it exists.
        /// </summary>
        Overwrite = 2
    }
}