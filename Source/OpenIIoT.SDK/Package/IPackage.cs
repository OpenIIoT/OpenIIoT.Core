using System;
using OpenIIoT.SDK.Packaging.Manifest;

namespace OpenIIoT.SDK.Package
{
    /// <summary>
    ///     Defines the interface for Package objects.
    /// </summary>
    public interface IPackage : IPackageManifest
    {
        #region Public Properties

        /// <summary>
        ///     Gets the fully qualified filename of the archive file.
        /// </summary>
        string FileName { get; }

        /// <summary>
        ///     Gets the Fully Qualified Name of the Package.
        /// </summary>
        string FQN { get; }

        /// <summary>
        ///     Gets a value indicating whether the Package is signed.
        /// </summary>
        bool IsSigned { get; }

        /// <summary>
        ///     Gets a value indicating whether the Package is trusted.
        /// </summary>
        bool IsTrusted { get; }

        /// <summary>
        ///     Gets the time at which the archive was last modified, according to the host filesystem.
        /// </summary>
        DateTime ModifiedOn { get; }

        #endregion Public Properties
    }
}