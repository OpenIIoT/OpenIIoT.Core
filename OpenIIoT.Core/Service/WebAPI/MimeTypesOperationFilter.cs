using System.Web.Http.Description;
using Swashbuckle.Swagger;

namespace OpenIIoT.Core.Service.WebApi
{
    /// <summary>
    ///     The Swashbuckle Operation Filter which determines the Accept and Content-Type types supported by the API.
    /// </summary>
    public class MimeTypeOperationFilter : IOperationFilter
    {
        #region Private Fields

        /// <summary>
        ///     Supported Accept types
        /// </summary>
        private static readonly string[] SupportedAccepts = new[] { "application/json" };

        /// <summary>
        ///     Supported Content-Type types
        /// </summary>
        private static readonly string[] SupportedContentTypes = new[] { "application/json" };

        #endregion Private Fields

        #region Public Methods

        /// <summary>
        ///     Applies the operation to the Swashbuckle build pipeline.
        /// </summary>
        /// <param name="operation">The current Operation.</param>
        /// <param name="schemaRegistry">The current Schema Registry.</param>
        /// <param name="apiDescription">The current Api Description</param>
        public void Apply(Operation operation, SchemaRegistry schemaRegistry, ApiDescription apiDescription)
        {
            operation.consumes = SupportedAccepts;
            operation.produces = SupportedContentTypes;
        }

        #endregion Public Methods
    }
}