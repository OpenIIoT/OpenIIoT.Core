namespace OpenIIoT.SDK
{
    using System;
    using System.Collections.Generic;
    using OpenIIoT.SDK.Common;
    using OpenIIoT.SDK.Common.Provider.ItemProvider;

    /// <summary>
    ///     Manages the various subsystem Managers.
    /// </summary>
    public interface IApplicationManager : IManager
    {
        #region Public Properties

        /// <summary>
        ///     Gets the name of the application instance.
        /// </summary>
        string InstanceName { get; }

        /// <summary>
        ///     Gets the list of <see cref="IManager"/> instances managed by the <see cref="IApplicationManager"/>.
        /// </summary>
        IReadOnlyList<IManager> Managers { get; }

        /// <summary>
        ///     Gets the name of the product; retrieved from AssemblyInfo.cs.
        /// </summary>
        string ProductName { get; }

        /// <summary>
        ///     Gets the version of the product; retrieved from AssemblyInfo.cs.
        /// </summary>
        Version ProductVersion { get; }

        /// <summary>
        ///     Gets the settings for the Application.
        /// </summary>
        IApplicationSettings Settings { get; }

        IItemProvider ItemProvider { get; }

        #endregion Public Properties

        #region Public Methods

        /// <summary>
        ///     Returns the Manager from the list of Managers matching the specified Type.
        /// </summary>
        /// <typeparam name="T">The Type of the Manager to return.</typeparam>
        /// <returns>The requested Manager.</returns>
        T GetManager<T>()
            where T : IManager;

        #endregion Public Methods
    }
}