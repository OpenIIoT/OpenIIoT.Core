using System;
using System.Diagnostics.CodeAnalysis;

[module: SuppressMessage("StyleCop.CSharp.MaintainabilityRules", "SA1402:FileMayOnlyContainASingleClass", Justification = "Reviewed.")]

namespace OpenIIoT.SDK.Common.Exceptions
{
    /// <summary>
    ///     Represents errors that occur within the Configuration namespace.
    /// </summary>
    [ExcludeFromCodeCoverage]
    [Serializable]
    public class ConfigurationException : Exception
    {
        #region Public Constructors

        /// <summary>
        ///     Initializes a new instance of the <see cref="ConfigurationException"/> class.
        /// </summary>
        public ConfigurationException()
            : base()
        {
        }

        /// <summary>
        ///     Initializes a new instance of the <see cref="ConfigurationException"/> class with a specified error message.
        /// </summary>
        /// <param name="message">The message that describes the error.</param>
        public ConfigurationException(string message)
            : base(message)
        {
        }

        /// <summary>
        ///     Initializes a new instance of the <see cref="ConfigurationException"/> class with a specified error message and a
        ///     reference to the inner exception that is the cause of this exception.
        /// </summary>
        /// <param name="message">The error message that explains the reason for the exception.</param>
        /// <param name="innerException">
        ///     The exception that is the cause of the current exception, or a null reference if no inner exception is specified.
        /// </param>
        public ConfigurationException(string message, Exception innerException)
            : base(message, innerException)
        {
        }

        #endregion Public Constructors
    }

